public class SistemaCalculadora {

    public void Adicao(double num1,double  num2) {
        double resultado = num1 + num2;

        System.out.println("A adição de "  + num1 + " + " + num2  + " é igual a: " + resultado);

    }

    public void Subtracao(double num1, double num2) {
        double resultado = num1 - num2;

        System.out.println("A subtração de "  + num1 + " - " + num2  + " é igual a: " + resultado);

    }

    public void  Divisao(double num1,double num2){
        double resultado = num1 / num2;

        System.out.println("A Dvisão de "  + num1 + " / " + num2  + " é igual a: " + resultado);


    }

    public void Multiplicacao(double num1, double num2){
        double resultado = num1 * num2;

        System.out.println("A Multiplicação de "  + num1 + " * " + num2  + " é igual a: " + resultado);

    }



}
